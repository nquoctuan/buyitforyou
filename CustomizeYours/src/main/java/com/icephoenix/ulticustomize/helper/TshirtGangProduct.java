package com.icephoenix.ulticustomize.helper;

public class TshirtGangProduct {
	public String title;
	public String color;
	public String style;
	public String category;
	public String image;
	public String keywords;

	public TshirtGangProduct(String title, String color, String style,
			String category, String image, String keywords) {
		this.title = title;
		this.color = color;
		this.style = style;
		this.category = category;
		this.image = image;
		this.keywords = keywords;
	}
}